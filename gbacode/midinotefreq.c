const unsigned int midiToDdsVal(int note) {
	if (note<24) return 6384507;
	if (note>96) return 408608429;
	return ddsNoteFrequencies[note-24];
}

const int midiToFreq(int note) {
	if (note<24) return 40;
	if (note>96) return 2030;
	return canNoteFrequencies[note-24];
}